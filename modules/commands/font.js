module.exports.config = {
  name: 'font',
  version: '2.0.0',
  hasPermssion: 0,
  credits: 'CHAND TRICKER',
  description: 'Converts text into Bold Font',
  commandCategory: 'tools',
  usePrefix: true,
  usages: '<input>',
  cooldowns: 5,
};

module.exports.run = async ({ event, api, args }) => {
  const inputText = args.join(' ');

  const fontMap = {
    ' ': ' ', 'a': 'ð’‚', 'b': 'ð’ƒ', 'c': 'ð’„', 'd': 'ð’…', 'e': 'ð’†', 'f': 'ð’‡', 'g': 'ð’ˆ', 'h': 'ð’‰',
    'i': 'ð’Š', 'j': 'ð’‹', 'k': 'ð’Œ', 'l': 'ð’', 'm': 'ð’Ž', 'n': 'ð’', 'o': 'ð’', 'p': 'ð’‘', 'q': 'ð’’',
    'r': 'ð’“', 's': 'ð’”', 't': 'ð’•', 'u': 'ð’–', 'v': 'ð’—', 'w': 'ð’˜', 'x': 'ð’™', 'y': 'ð’š', 'z': 'ð’›',
    'A': 'ð‘¨', 'B': 'ð‘©', 'C': 'ð‘ª', 'D': 'ð‘«', 'E': 'ð‘¬', 'F': 'ð‘­', 'G': 'ð‘®', 'H': 'ð‘¯',
    'I': 'ð‘°', 'J': 'ð‘±', 'K': 'ð‘²', 'L': 'ð‘³', 'M': 'ð‘´', 'N': 'ð‘µ', 'O': 'ð‘¶', 'P': 'ð‘·', 'Q': 'ð‘¸',
    'R': 'ð‘¹', 'S': 'ð‘º', 'T': 'ð‘»', 'U': 'ð‘¼', 'V': 'ð‘½', 'W': 'ð‘¾', 'X': 'ð‘¿', 'Y': 'ð’€', 'Z': 'ð’',
  };

  const outputText = inputText
    .split('')
    .map(char => fontMap[char] || char) // Replace characters with stylized versions
    .join('');

  return api.sendMessage(outputText, event.threadID, event.messageID);
};
